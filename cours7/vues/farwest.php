<h1>Far west</h1>
<img src="./vues/images/farwest.jpeg" alt="./images/farwest.jpeg" />
<p>L'Ouest amé
ricain, aussi appelé Far West (« Grand Ouest » en anglais), est une région située à 
l'ouest des États
-
Unis. Sa définition a évolué avec le temps, puisque la population a colonisé les 
terres intérieures en se dirigeant vers l'océan Pacifique. Cette colonisa
tion est appelée la conquête 
de l'Ouest. Le Far West est une région où se développe une société originale, où se croisent des 
individus d'origines et d'horizon très différents. L'Amérique mythique des montagnes Rocheuses, où 
s'affrontent les tribus amérind
iennes et les pionniers venus fonder les États
-
Unis est à jamais 
symbolisée, dans la mémoire des peuples, par des déserts rouges à perte de vue et des montagnes 
sculptées par l'érosion.</p>